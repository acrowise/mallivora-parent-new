package com.mallivora.fabric.service.config;

public class Policy {

    private String Type;
    private String Rule;

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getRule() {
        return Rule;
    }

    public void setRule(String rule) {
        Rule = rule;
    }
}
